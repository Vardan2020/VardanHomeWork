public class LesonOneExOne {
    public static void main(String[] args) {

        int x = 0;
        if (x > 0) {
            System.out.println(x * 2);
        } else if (x < 0) {
            System.out.println(x * 3);
        } else if (x == 0) {
            x = 10;
            System.out.println(x);
        }
    }
}
